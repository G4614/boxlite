// BoxCA is an ephemeral ECDSA P-256 certificate authority for MITM.
//
// Carries an optional ErrSink so cert-generation failures surface to
// the operator via the same channel as the gvproxy goroutine errors.
// A persistent cert-gen failure is security-shaped: it means MITM is
// silently disabled for that hostname (the guest sees a TLS handshake
// error and may fall back to a non-intercepted path), so it needs to
// be observable, not just logged. See SetErrSink below.
type BoxCA struct {
	cert      *x509.Certificate
	key       *ecdsa.PrivateKey
	certPEM   []byte
	certCache sync.Map // hostname -> *tls.Certificate

	// errSink is optional — nil means "MITM has no observable sink, fall
	// back to logrus-only" which is the pre-#634 behavior. Production
	// wiring sets it via SetErrSink in gvproxy_create.
	errSink *ErrSink
}

// SetErrSink wires an ErrSink into the BoxCA. mitmAndForward routes
// cert-generation failures through it via reportCertGenFailure so the
// Rust-side polling consumer (instance::poll_and_route_once) picks
// them up. Per-connection MITM events (TLS handshake mismatch,
// upstream dial fail, websocket hijack) remain log-only — they're
// expected failure modes, not silent regressions.
func (ca *BoxCA) SetErrSink(sink *ErrSink) {
	ca.errSink = sink
}

// reportCertGenFailure pushes a hostname-tagged event onto the
// configured ErrSink, no-op if no sink is set. Pulled into a method
// so the test suite can drive it without needing a real CA / cert gen
// failure.
//
// Pre-#634, the cert-gen failure path was `logrus.Error + close conn`,
// invisible to the Rust runtime. Post-fix, this method makes the
// failure observable; combined with the polling consumer added in this
// PR, an operator sees it in tracing under
// target=gvproxy.runtime_error with source=mitm.GenerateHostCert and
// the hostname embedded in the cause string.
func (ca *BoxCA) reportCertGenFailure(hostname string, cause error) {
	if ca.errSink == nil {
		return
	}
	ca.errSink.Runtime("mitm.GenerateHostCert", fmt.Errorf("hostname=%q: %w", hostname, cause))
}

// NewBoxCAFromPEM reconstructs a BoxCA from PEM-encoded cert and key.
// The cert/key are generated by the Rust side and passed via GvproxyConfig JSON.
func NewBoxCAFromPEM(certPEM, keyPEM []byte) (*BoxCA, error) {
	block, _ := pem.Decode(certPEM)
	if block == nil || block.Type != "CERTIFICATE" {
		return nil, fmt.Errorf("failed to decode CA certificate PEM")
	}
	cert, err := x509.ParseCertificate(block.Bytes)
	if err != nil {
		return nil, fmt.Errorf("failed to parse CA certificate: %w", err)
	}

	keyBlock, _ := pem.Decode(keyPEM)
	if keyBlock == nil {
		return nil, fmt.Errorf("failed to decode CA key PEM")
	}

	// rcgen uses PKCS8 format ("PRIVATE KEY"), try that first
	rawKey, err := x509.ParsePKCS8PrivateKey(keyBlock.Bytes)
	if err != nil {
		// Fallback to SEC1 format ("EC PRIVATE KEY")
		rawKey, err = x509.ParseECPrivateKey(keyBlock.Bytes)
		if err != nil {
			return nil, fmt.Errorf("failed to parse CA key (tried PKCS8 and SEC1): %w", err)
		}
	}

	ecKey, ok := rawKey.(*ecdsa.PrivateKey)
	if !ok {
		return nil, fmt.Errorf("CA key is not ECDSA (got %T)", rawKey)
	}

	return &BoxCA{
		cert:    cert,
		key:     ecKey,
		certPEM: certPEM,
	}, nil
}

// CACertPEM returns the CA certificate in PEM format.
func (ca *BoxCA) CACertPEM() []byte {
	return ca.certPEM
}

// CACertPool returns an x509.CertPool containing this CA's certificate.
func (ca *BoxCA) CACertPool() (*x509.CertPool, error) {
	pool := x509.NewCertPool()
	pool.AddCert(ca.cert)
	return pool, nil
}

// GenerateHostCert generates a TLS certificate for the given hostname, signed by this CA.
// Results are cached per-hostname.
const maxCertCacheSize = 10000

func (ca *BoxCA) GenerateHostCert(hostname string) (*tls.Certificate, error) {
	if cached, ok := ca.certCache.Load(hostname); ok {
		tlsCert := cached.(*tls.Certificate)
		// Check TTL: regenerate if cert has expired
		if tlsCert.Leaf != nil && time.Now().Before(tlsCert.Leaf.NotAfter) {
			return tlsCert, nil
		}
		// Expired — fall through to regenerate
		ca.certCache.Delete(hostname)
	}

	key, err := ecdsa.GenerateKey(elliptic.P256(), rand.Reader)
	if err != nil {
		return nil, err
	}

	serial, err := rand.Int(rand.Reader, new(big.Int).Lsh(big.NewInt(1), 128))
	if err != nil {
		return nil, err
	}

	now := time.Now()
	template := &x509.Certificate{
		SerialNumber:          serial,
		NotBefore:             now.Add(-1 * time.Minute),
		NotAfter:              now.Add(24 * time.Hour),
		KeyUsage:              x509.KeyUsageDigitalSignature | x509.KeyUsageKeyEncipherment,
		ExtKeyUsage:           []x509.ExtKeyUsage{x509.ExtKeyUsageServerAuth},
		BasicConstraintsValid: true,
	}

	if ip := net.ParseIP(hostname); ip != nil {
		template.IPAddresses = []net.IP{ip}
	} else {
		template.DNSNames = []string{hostname}
	}

	certDER, err := x509.CreateCertificate(rand.Reader, template, ca.cert, &key.PublicKey, ca.key)
	if err != nil {
		return nil, err
	}

	leaf, _ := x509.ParseCertificate(certDER)
	tlsCert := &tls.Certificate{
		Certificate: [][]byte{certDER, ca.cert.Raw},
		PrivateKey:  key,
		Leaf:        leaf, // stored for TTL check
	}

	// Evict expired entries and enforce max size.
	// sync.Map doesn't have a size, so we count during eviction.
	cacheSize := 0
	ca.certCache.Range(func(key, val any) bool {
		if cert, ok := val.(*tls.Certificate); ok && cert.Leaf != nil && now.After(cert.Leaf.NotAfter) {
			ca.certCache.Delete(key) // expired
		} else {
			cacheSize++
		}
		return true
	})
	if cacheSize >= maxCertCacheSize {
		// Over limit even after TTL eviction — clear oldest half
		evicted := 0
		ca.certCache.Range(func(key, _ any) bool {
			ca.certCache.Delete(key)
			evicted++
			return evicted < cacheSize/2
		})
	}

	actual, loaded := ca.certCache.LoadOrStore(hostname, tlsCert)
	if loaded {
		return actual.(*tls.Certificate), nil
	}
	return tlsCert, nil
}

// substituteHeaders replaces secret placeholders in request headers and URL query.
func substituteHeaders(req *http.Request, secrets []SecretConfig) {
	if len(secrets) == 0 {
		return
	}

	pairs := make([]string, 0, len(secrets)*2)
	for _, s := range secrets {
		pairs = append(pairs, s.Placeholder, s.Value)
	}
	r := strings.NewReplacer(pairs...)

	for key, vals := range req.Header {
		for i, v := range vals {
			req.Header[key][i] = r.Replace(v)
		}
	}

	if req.URL != nil && req.URL.RawQuery != "" {
		req.URL.RawQuery = r.Replace(req.URL.RawQuery)
	}
}

// resolveUpstreamTLS returns the TLS config for upstream connections.
// Uses the first override if provided, otherwise creates a default config with ServerName.
func resolveUpstreamTLS(hostname string, overrides ...*tls.Config) *tls.Config {
	if len(overrides) > 0 && overrides[0] != nil {
		return overrides[0]
	}
	return &tls.Config{ServerName: hostname}
}

// SecretHostMatcher provides O(1) lookup for whether a hostname has secrets.
type SecretHostMatcher struct {
	exactHosts       map[string]bool
	wildcardSuffixes []string
	secrets          []SecretConfig
}

// NewSecretHostMatcher builds a matcher from secret configs.
func NewSecretHostMatcher(secrets []SecretConfig) *SecretHostMatcher {
	m := &SecretHostMatcher{
		exactHosts: make(map[string]bool),
		secrets:    secrets,
	}

	for _, s := range secrets {
		for _, host := range s.Hosts {
			h := strings.ToLower(host)
			if strings.HasPrefix(h, "*.") {
				suffix := h[1:] // e.g., ".openai.com"
				m.wildcardSuffixes = append(m.wildcardSuffixes, suffix)
			} else {
				m.exactHosts[h] = true
			}
		}
	}

	return m
}

// matchesWildcard checks if hostname matches a wildcard suffix (e.g., ".foo.com").
// Only single-level subdomains match: "bar.foo.com" matches but "sub.bar.foo.com" doesn't.
func matchesWildcard(hostname, suffix string) bool {
	return strings.HasSuffix(hostname, suffix) &&
		!strings.Contains(hostname[:len(hostname)-len(suffix)], ".")
}

// Matches returns true if hostname has associated secrets.
func (m *SecretHostMatcher) Matches(hostname string) bool {
	h := strings.ToLower(hostname)
	if m.exactHosts[h] {
		return true
	}
	for _, suffix := range m.wildcardSuffixes {
		if matchesWildcard(h, suffix) {
			return true
		}
	}
	return false
}

// SecretsForHost returns all secrets whose Hosts list includes hostname.
func (m *SecretHostMatcher) SecretsForHost(hostname string) []SecretConfig {
	h := strings.ToLower(hostname)
	var result []SecretConfig
	for _, s := range m.secrets {
		for _, host := range s.Hosts {
			host = strings.ToLower(host)
			if host == h {
				result = append(result, s)
				break
			}
			if strings.HasPrefix(host, "*.") && matchesWildcard(h, host[1:]) {
				result = append(result, s)
				break
			}
		}
	}
	return result
}
